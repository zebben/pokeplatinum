FollowMonInfo *FollowMon_GetMetadata(u16 species, u8 form)
{
  form = Pokemon_SanitizeFormId(species, form);

  if (species == SPECIES_SHAYMIN && form == 1) {
    return &sFollowMonShayminSkyInfo;
  }

  if (species == SPECIES_GIRATINA && form == 1) {
    return &sFollowMonGiratinaOriginInfo;
  }

  if (species == SPECIES_NONE || species > SPECIES_ARCEUS) {
    return NULL;
  }

  if (sFollowMonInfo[species].tooTall == 0 && sFollowMonInfo[species].movementFlags == 0 && sFollowMonInfo[species].unk0 == 0 && sFollowMonInfo[species].unk3 == 0) {
    return &sFollowMonDefaultInfo;
  }

  return &sFollowMonInfo[species];
}

