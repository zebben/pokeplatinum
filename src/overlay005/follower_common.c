static int FollowMon_GetObjectFlags(MapObject *mapObj)
{
  return MapObject_GetDataAt(mapObj, FOLLOW_MON_OBJECT_PARAM_FLAGS) & FOLLOW_MON_OBJECT_FLAGS_SAVE_MASK;
}

static void FollowMon_SetObjectFlags(MapObject *mapObj, int flags)
{
  MapObject_SetDataAt(mapObj, flags & FOLLOW_MON_OBJECT_FLAGS_SAVE_MASK, FOLLOW_MON_OBJECT_PARAM_FLAGS);
}

static void FollowMon_SetObjectFlag(MapObject *mapObj, int flag, BOOL enabled)
{
  int flags = FollowMon_GetObjectFlags(mapObj);

  flags &= ~flag;

  if (enabled) {
    flags |= flag;
  }

  FollowMon_SetObjectFlags(mapObj, flags);
}

static void FollowMon_SetObjectShiny(MapObject *mapObj, BOOL isShiny)
{
  FollowMon_SetObjectFlag(mapObj, FOLLOW_MON_OBJECT_FLAG_SHINY, isShiny);
}

static void FollowMon_SetObjectFormParams(MapObject *mapObj, u16 species, u8 form)
{
  FollowMonInfo *info = FollowMon_GetMetadata(species, form);
  int params = 0;

  if (info != NULL) {
    params = (info->tooTall << 8) | info->movementFlags;
  }

  MapObject_SetDataAt(mapObj, params, FOLLOW_MON_OBJECT_PARAM_FORM);
}

void FollowMon_SetObjectParams(MapObject *mapObj, u16 species, u8 form, BOOL isShiny)
{
  MapObject_SetDataAt(mapObj, species, FOLLOW_MON_OBJECT_PARAM_SPECIES);
  FollowMon_SetObjectFormParams(mapObj, species, form);
  FollowMon_SetObjectShiny(mapObj, isShiny);
}

