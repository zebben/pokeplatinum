static u8 FollowMon_GetGraphicsSlotCount(u16 species)
{
  if (FollowMon_HasGenderDifference(species)) {
    return 2;
  }

  return FollowMon_GetMaxFormIndex(species) + 1;
}

int FollowMon_GetMetadataIndex(u16 species)
{
  int index = 0;
  int i;

  if (species == SPECIES_NONE || species > SPECIES_ARCEUS) {
    return SPECIES_NONE;
  }

  for (i = SPECIES_BULBASAUR; i < species; i++) {
    index += FollowMon_GetGraphicsSlotCount(i);
  }

  return index;
}

