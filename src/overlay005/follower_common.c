  case SPECIES_UNOWN:
    return UNOWN_FORM_COUNT - 1;
  case SPECIES_DEOXYS:
    return DEOXYS_FORM_COUNT - 1;
  case SPECIES_BURMY:
    return BURMY_FORM_COUNT - 1;
  case SPECIES_WORMADAM:
